<p>According to <a href="http://artportal.hu/lexikon/muveszek/papp_tibor">Tibor Papp</a> the logo-mandala is a characteristic type of visual literary works based on visible language that became known mainly by the works of concrete and spacialist poets.<br>
Its main features are</p>
<ul>
	<li>1) has a circular or square shape,</li>
	<li>2) has a centre point,</li>
	<li>3) symmetrical,</li>
	<li>4) the direction of reading is reversible along its axis,</li>
	<li>5) the words can often be read backwards.</li>
</ul>
<p>In the logo-mandalas the written form of words serves the polyphonic reading,<br>
the richness of the visual content unfolds only after a long contemplation. The logo-mandala is the genre of meditation.</p>
