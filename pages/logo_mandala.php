<p><a href="http://artportal.hu/lexikon/muveszek/papp_tibor">Papp Tibor</a> megfogalmazásában: a látható nyelvre alapozott vizuális irodalmi művek egyik jellegzetes típusa a logo-mandala, mely főleg a konkrét- és a spacialista-költők munkái nyomán vált ismertté.<br />
Legfőbb tulajdonsága, hogy</p>
<ul>
	<li>1) formája kör vagy négyzet alakú,</li>
	<li>2) középpontja van,</li>
	<li>3) szimmetrikus,</li>
	<li>4) olvasata a tengelyek mentén megfordítható,</li>
	<li>5) szavai sokszor visszafelé is olvashatók.</li>
</ul>
<p>A logo-mandalában a szavak írott képe a többszólamú olvasat szolgálatában áll,<br>
a látványban rejlő tartalom gazdagsága többnyire csak hosszabb szemlélődés után<br>bontakozik ki. A logo-mandala az elmélkedés műfaja.</p>
